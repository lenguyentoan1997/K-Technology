    public void loadReport() {

        String fromDate = fromDateFormat.format(txtFromDate.getDate());
        String toDate = toDateFormat.format(txtToDate.getDate());
        //put parameters Jasper Report
        HashMap a = new HashMap();
        a.put("fromDate", fromDate);
        a.put("toDate", toDate);
        try {
            JasperDesign jDesign = JRXmlLoader.load("H:\\Project\\ProjectJavaSem2\\K&Technology\\src\\View\\JasperReport\\reportStatistics.jrxml");
            JasperReport jReport = JasperCompileManager.compileReport(jDesign);
            JasperPrint jPrint = JasperFillManager.fillReport(jReport, a, DatabaseConnection.openConnection());
            JasperViewer.viewReport(jPrint, false);
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

