    private void btnQueryChartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQueryChartActionPerformed

        try {
            String fromDate = fromDateFormat.format(txtFromDate.getDate());
            String toDate = toDateFormat.format(txtToDate.getDate());
            String query = "select date,billprice from dbo.bill where date between '" + fromDate + "' and'" + toDate + "'";
            JDBCCategoryDataset dataSet = new JDBCCategoryDataset(DatabaseConnection.openConnection(), query);

            JFreeChart jChart = ChartFactory.createLineChart("", "Date Time", "Bill Price", dataSet, PlotOrientation.VERTICAL, false, true, false);
            CategoryPlot categoryPlot = jChart.getCategoryPlot();
            categoryPlot.setRangeGridlinePaint(Color.ORANGE);

            ChartPanel chartPanel = new ChartPanel(jChart);
            panelChart.removeAll();
            panelChart.setLayout(new java.awt.BorderLayout());
            panelChart.add(chartPanel, BorderLayout.CENTER);
            panelChart.validate();

        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnQueryChartActionPerformed

